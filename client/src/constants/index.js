export const renderPagination = (totalPages,handlePageChange,currentPage,pagesToShow) => {
  const pageButtons = [];

  for (let i = 1; i <= totalPages; i++) {
    if (i === 1 || i === totalPages) {
      // Show first and last pages
      pageButtons.push(
        <a
          key={i}
          onClick={() => handlePageChange(i)}
          className={
            "relative inline-flex items-center px-4 py-2 text-sm font-semibold " +
            (currentPage === i
              ? "bg-red-600 text-white focus:z-20 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-600"
              : "text-gray-900 ring-1 ring-inset ring-gray-400 hover:bg-red-400 focus:z-20 focus:outline-offset-0")
          }
        >
          {i}
        </a>
      );
    } else if (i >= currentPage - 2 && i <= currentPage + 2) {
      // Show nearby pages
      pageButtons.push(
        <a
          key={i}
          onClick={() => handlePageChange(i)}
          className={
            "relative inline-flex items-center px-4 py-2 text-sm font-semibold " +
            (currentPage === i
              ? "bg-red-600 text-white focus:z-20 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-600"
              : "text-gray-900 ring-1 ring-inset ring-gray-400 hover:bg-red-400 focus:z-20 focus:outline-offset-0")
          }
        >
          {i}
        </a>
      );
    }
  }

  if (totalPages > pagesToShow) {
    // Add ellipsis after the first few pages
    if (currentPage > pagesToShow - 2) {
      pageButtons.splice(
        1,
        0,
        <span
          className="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-900 ring-1 ring-inset ring-gray-400 focus:outline-offset-0"
          key="ellipsis1"
        >
          ...
        </span>
      );
    }
    // Add ellipsis before the last few pages
    if (currentPage < totalPages - (pagesToShow - 2)) {
      pageButtons.splice(
        pageButtons.length - 1,
        0,
        <span
          className="relative inline-flex items-center px-4 py-2 text-sm font-semibold text-gray-900 ring-1 ring-inset ring-gray-400 focus:outline-offset-0"
          key="ellipsis2"
        >
          ...
        </span>
      );
    }
  }

  return pageButtons;
};
